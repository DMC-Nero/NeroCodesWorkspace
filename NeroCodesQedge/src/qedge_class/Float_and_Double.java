package qedge_class;

public class Float_and_Double {
	float f1 = 2.5F; // valid
	float f2 = 10; // valid
	float f3 = 10L; // valid
	double d0 = 2.5; // valid
	double d1 = 2.5f; // valid
	double d2 = 10; // valid
	double d3 = 10L; // valid
	// other example
	int i = 10;
	long l1 = 10L;
	float f5 = 2.5F;
	double d12 = 2.5D;
	// Explicit type casting
	int j = 10;
	byte b = (byte) j;
	float f12 = 2.5F;
	float f13 = (float) d1;
}
