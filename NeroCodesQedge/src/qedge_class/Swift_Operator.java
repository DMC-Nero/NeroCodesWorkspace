package qedge_class;

public class Swift_Operator {
	public static void main(String[] args) {
		int x = 10;
		x = x >> 1;
		System.out.println(x);

		x = x >> 1;
		System.out.println(x);

		int y = 10;
		System.out.println(y << 2);

		System.out.println(y << 3);

		int z = 100;
		System.out.println(z);
	}

}
