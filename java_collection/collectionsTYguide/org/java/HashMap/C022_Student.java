package org.java.HashMap;

public class C022_Student {

	private String firstName;
	private String lastName;
	
	public C022_Student(String firstName, String lastName) {
		super();
		this.firstName = firstName;
		this.lastName = lastName;
	}

	@Override
	public String toString() {
		return "C022_Student [firstName=" + firstName + ", lastName=" + lastName + "]";
	}
	
}
